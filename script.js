    function changeTheme(color) {
      const themeColors = {
        'blue':   { primary:'bg-blue-600',   secondary:'border-blue-500',  progress:'bg-blue-500',  badge: 'bg-blue-100 text-blue-800' },
        'red':    { primary:'bg-red-600',    secondary:'border-red-500',   progress:'bg-red-500',   badge: 'bg-red-100 text-red-800' },
        'green':  { primary:'bg-green-600',  secondary:'border-green-500', progress:'bg-green-500', badge: 'bg-green-100 text-green-800' },
        'purple': { primary:'bg-purple-600', secondary:'border-purple-500',progress:'bg-purple-500',badge: 'bg-purple-100 text-purple-800' },
        'yellow': { primary:'bg-yellow-600', secondary:'border-yellow-500',progress:'bg-yellow-500',badge: 'bg-yellow-100 text-yellow-800' }
      };
      const selected = themeColors[color];

      // Header
      const header = document.getElementById('headerColor');
      header.className = header.className
        .replace(/bg-(blue|red|green|purple|yellow)-600/g, selected.primary);

      // Borders (section headings and profile border)
      document.querySelectorAll('[class*="border-"][class*="-500"]').forEach(el=>{
        el.className = el.className.replace(/border-(blue|red|green|purple|yellow)-500/g, selected.secondary);
      });

      // Progress bars
      document.querySelectorAll('.progress-fill').forEach(el=>{
        el.className = el.className.replace(/bg-(blue|red|green|purple|yellow)-500/g, selected.progress);
      });

      // Badges (dates)
      document.querySelectorAll('[class*="bg-"][class*="-100"].text-blue-800, [class*="bg-"][class*="-100"].text-red-800, [class*="bg-"][class*="-100"].text-green-800, [class*="bg-"][class*="-100"].text-purple-800, [class*="bg-"][class*="-100"].text-yellow-800')
        .forEach(el=>{
          el.className = el.className
            .replace(/bg-(blue|red|green|purple|yellow)-100/g, selected.badge.split(' ')[0])
            .replace(/text-(blue|red|green|purple|yellow)-800/g, selected.badge.split(' ')[1]);
        });
    }

