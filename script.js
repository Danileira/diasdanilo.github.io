    // Font size
    document.getElementById('fontSize').addEventListener('change', function(){
      const size = this.value;
      const cv = document.getElementById('cvTemplate');
      cv.classList.remove('text-sm','text-lg');
      if (size === 'small') cv.classList.add('text-sm');
      if (size === 'large') cv.classList.add('text-lg');
    });

    // Layout (efeito visual simples)
    document.getElementById('layoutSelect').addEventListener('change', function(){
      const layout = this.value;
      const header = document.getElementById('headerColor');
      header.classList.remove('bg-gradient-to-r','from-blue-600','to-blue-400','from-blue-500','to-yellow-600');
      if (layout === 'modern') header.classList.add('bg-gradient-to-r','from-blue-600','to-blue-400');
      if (layout === 'creative') header.classList.add('bg-gradient-to-r','from-blue-500','to-yellow-600');
    });

